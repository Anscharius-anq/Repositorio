print("cadena de texto")

# el primer caracter despues del backslash es una letra que indica el tipo de escape

print("1.","cadena de \"texto\"") # \" es un escape para comillas dobles
print("2.","cadena de \'texto\'") # \' es un escape para comillas simples
print("3.","cadena de \\texto\\") # \\ es un escape para backslash
print("4.","cadena de \ntexto") # \n es un escape para nueva linea
print("5.","cadena de \ttexto") # \t es un escape para tabulacion
print("6.","cadena de \btexto") # \b es un escape para retroceso
print("7.","cadena de \ftexto") # \f es un escape para salto de pagina
print("8.","cadena de \vtexto") # \v es un escape para tabulacion vertical
print("9.","cadena de \rtexto") # \r es un escape para retorno de carro, mueve el cursor al inicio, sobreescribe el texto

